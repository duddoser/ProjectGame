package com.example.projectgame.models;

public class User {
    private String name;
    private String email;
    private String unique_id;
    private String password;
    private String wood;
    private String iron;
    private String metal;
    private String stone;
    private String obtain_res;
    private String give_res;
    private String obtain_num;
    private String give_num;
    private String code;

    public void setUnique_id(String unique_id) {
        this.unique_id = unique_id;
    }

    public String getPassword() {
        return password;
    }

    public String getWood() {
        return wood;
    }

    public void setWood(String wood) {
        this.wood = wood;
    }

    public String getIron() {
        return iron;
    }

    public void setIron(String iron) {
        this.iron = iron;
    }

    public String getMetal() {
        return metal;
    }

    public void setMetal(String metal) {
        this.metal = metal;
    }

    public String getStone() {
        return stone;
    }

    public void setStone(String stone) {
        this.stone = stone;
    }

    public String getObtain_res() {
        return obtain_res;
    }

    public void setObtain_res(String obtain_res) {
        this.obtain_res = obtain_res;
    }

    public String getGive_res() {
        return give_res;
    }

    public void setGive_res(String give_res) {
        this.give_res = give_res;
    }

    public String getObtain_num() {
        return obtain_num;
    }

    public void setObtain_num(String obtain_num) {
        this.obtain_num = obtain_num;
    }

    public String getGive_num() {
        return give_num;
    }

    public void setGive_num(String give_num) {
        this.give_num = give_num;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getUnique_id() {
        return unique_id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
